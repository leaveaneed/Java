class Parent {
    public void displayMessage() {
        System.out.println("Метод суперкласса");
    }
}

class Child extends Parent {
    // Ошибка: неправильное имя метода (без @Override ошибка может остаться незамеченной)
    @Override
    public void displayMessages() { // Ошибка компиляции из-за неверного имени
        System.out.println("Неправильно переопределенный метод");
    }
}

public class Main {
    public static void main(String[] args) {
        Parent obj = new Child();
        obj.displayMessage();
    }
}

/*Аннотация @Override помогает обнаружить ошибки при попытке переопределить метод. 
Если метод с аннотацией @Override не соответствует методу в суперклассе, компилятор выдаст ошибку.
Аннотация @Override требует, чтобы метод точно соответствовал методу в суперклассе.
Если метод в подклассе не совпадает по имени, типу возвращаемого значения или параметрам, компилятор выдаст ошибку.
Это помогает избежать случайных ошибок, особенно при рефакторинге или больших кодовых базах.
 */